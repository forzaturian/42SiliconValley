#include "libft.h"

size_t	ft_numlen(int num)
{
	int i;

	i = 0;
	if (num == 0)
		return (1);
	if (num < i)
		i++;
	while (num != 0)
	{
		num = num / 10;
		i++;
	}
	return (i);
}
