#include "libft.h"

void	ft_clean_int_arr(int ***map)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (!(map)[i])
		return ;
	while ((*map)[i])
		i++;
	while (j < i)
	{
		free((*map)[j]);
		j++;
	}
	free(*map);
}
