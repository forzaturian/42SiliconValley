#include <unistd.h>
int		main(int ac, char *av[])
{
	int		i;

	if (ac != 2)
		write(1, "\n", 1);
	else
	{
		i = 0;
		while (av[1][i])
		{
			i++;
		}
		i--;
		while (av[1][i] == '\t' || av[1][i] == ' ')
			i--;
		while (i > 0)
		{
			if (av[1][i] == '\t' || av[1][i] == ' ')
			{
				break;
			}
			else 
			{
				i--;
			}
		}
		i++;
		while (av[1][i] && (av[1][i] != '\t' || av[1][i] != ' '))
		{
			write(1, &av[1][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
